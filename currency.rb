
def usd_to_eur(money)
  money * 0.86
end

def eur_to_usd(money)
  money / 0.86
end

def usd_to_jpy(money)
  money * 110.15
end

def jpy_to_usd(money)
  money / 110.15
end

def usd_to_gbp(money)
  money * 0.75
end

def gbp_to_usd(money)
  money / 0.75
end

def usd_to_aud(money)
  money * 1.35
end

def aud_to_usd(money)
  money / 1.35
end
